drop table if exists PRODUCT;

create table PRODUCT(
    ID INTEGER AUTO_INCREMENT,
    NAME VARCHAR(128) NOT NULL,
    PRICE DECIMAL NOT NULL,
    STOCK INTEGER NOT NULL,
    SELLER VARCHAR(128) NOT NULL,
    CATEGORY VARCHAR(128) NOT NULL,
    SUBCATEGORY VARCHAR(128) NOT NULL,
    FIT VARCHAR(128),
    MATERIAL VARCHAR(128),
    PRODTYPE VARCHAR(128),
    PICBYTE Varchar(128),
    SELLERUSERNAME VARCHAR(128)
);

drop table if exists USER;

create table USER(
    USER_ID INTEGER AUTO_INCREMENT,
    FIRSTNAME VARCHAR(128) NOT NULL,
    LASTNAME VARCHAR(128) NOT NULL,
    USERNAME VARCHAR(128) NOT NULL,
    GENDER VARCHAR(128) NOT NULL,
    PASSWORD VARCHAR(128) NOT NULL,
    CONFIRMPASSWORD VARCHAR(128) NOT NULL,
    ROLES VARCHAR(128) NOT NULL,
    ENABLED BOOLEAN,
    WISHLIST VARCHAR(255),
    CART VARCHAR(255),
    CARTPRODAMT VARCHAR(255),
    BILLINGAMT DOUBLE
);

drop table if exists CONFIRMATIONTOKEN;

create table CONFIRMATIONTOKEN(
     TOKENID INTEGER AUTO_INCREMENT,
     CREATEDDATE DATE NOT NULL,
     CONFIRMATIONTOKEN VARCHAR(128) NOT NULL,
     USER_ID INTEGER NOT NULL
);

drop table if exists ORDERHISTORY;

create table ORDERHISTORY(
      ID INTEGER AUTO_INCREMENT,
      CREATEDDATE DATE,
      USER_ID INTEGER,
      CART VARCHAR(255),
      CARTPRODQTY VARCHAR(255),
      BILLINGAMT DOUBLE
);

